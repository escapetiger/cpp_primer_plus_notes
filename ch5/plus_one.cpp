// plus_one.cpp - the increment opertor
#include <iostream>
int main(void)
{
    int a = 20, b = 20;
    std::cout << "a    = " << a << ":   b = " << b << std::endl;
    std::cout << "a++  = " << a++ << ": ++b = " << ++b << std::endl;
    std::cout << "a    = " << a << ":   b = " << b << std::endl;
    return 0;
}
